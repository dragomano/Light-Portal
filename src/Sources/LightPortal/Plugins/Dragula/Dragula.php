namespace Bugo\LightPortal\Plugins\Dragula;

use Bugo\Compat\Theme;
use Bugo\Compat\User;
use Bugo\LightPortal\Enums\Placement;
use Bugo\LightPortal\Enums\PortalHook;
use Bugo\LightPortal\Plugins\Event;
use Bugo\LightPortal\Plugins\HookAttribute;
use Bugo\LightPortal\Plugins\Plugin;
use Bugo\LightPortal\Plugins\PluginAttribute;

if (! defined('LP_NAME'))
	die('No direct access...');

/**
 * Generated by PluginMaker
 */
#[PluginAttribute]
class Dragula extends Plugin
{
	#[HookAttribute(PortalHook::init)]
	public function init(): void
	{
		if (! User::$me->allowedTo('admin_forum'))
			return;

		foreach (Placement::all() as $alias => $placement) {
			Theme::addInlineCss(PHP_EOL . "\t\t" . '[data-panel="' . $alias . '"].gu-over:before {
			content: "' . $placement . '";
		}');
		}

		$this->loadExternalResources([
			['type' => 'css', 'url' => 'https://cdn.jsdelivr.net/npm/dragula@3/dist/dragula.min.css'],
			['type' => 'js',  'url' => 'https://cdn.jsdelivr.net/npm/dragula@3/dist/dragula.min.js'],
		]);

		Theme::addInlineJavaScript('handleDragging()', true);
	}

	#[HookAttribute(PortalHook::credits)]
	public function credits(Event $e): void
	{
		$e->args->links[] = [
			'title' => 'Dragula',
			'link' => 'https://github.com/bevacqua/dragula/',
			'author' => 'Nicolas Bevacqua',
			'license' => [
				'name' => 'the MIT License',
				'link' => 'https://github.com/bevacqua/dragula/?tab=MIT-1-ov-file#readme'
			]
		];
	}
}
